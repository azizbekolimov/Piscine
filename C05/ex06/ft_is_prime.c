int	ft_is_prime(int nb)
{
	int half;
	int n;

	n = 5;
	half = nb / 2;
	if (nb == 2 || nb == 3)
		return (1);
	if ((nb % 2) == 0 || (nb % 3) == 0 || (nb <= 1))
		return (0);
	while (n <= half)
	{
		if (nb % n == 0 || nb % (n + 2) == 0)
			return (0);
		n += 6;
	}
	return (1);
}
